package day0725;

public class Demo08 {
    public static void main(String[] args) {
        int n = 5;
        n++;
        System.out.println(n);
        n = 5;
        n--;
        System.out.println(n);
        n = 5;
        ++n;
        System.out.println(n);
        n = 5;
        --n;
        System.out.println(n);
    }
}
